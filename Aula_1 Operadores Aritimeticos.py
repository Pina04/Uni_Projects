num1 = 5
num2 = 2

print(type(num1), type(num2))

som = num1 + num2
print("Som:", som, type(som))

sub = num1 - num2
print("Sub:", sub, type(sub))

mult = num1 * num2
print("Mult:", mult, type(mult))

div = num1 / num2
print("Div:", div, type(div))

pot = num1 ** num2
print("Pot:", pot, type(pot))

div_int = num1 // num2
print("Div_int:", div_int, type(div_int))

rest = num1 % num2
print("Rest:", rest, type(rest))
